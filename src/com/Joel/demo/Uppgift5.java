package com.Joel.demo;

public class Uppgift5 {
    public static void main(String[] args) {
        String Name = "Benny";
        switch (Name) {
            case "Anton":
                System.out.println("It is Anton");
                break;
            case "Benny":
                System.out.println("It is Benny");
                break;
            default:
                System.out.println("no match");
        }
    }
}
