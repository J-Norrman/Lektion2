package com.Joel.demo;

public class Uppgift3_4 {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; ) {
            if ( i < 10) {
                System.out.println(i + " is less than 10");
            } else {
                System.out.println(i + " is equals to 10");
            }
            i++;
        }
    }
}
